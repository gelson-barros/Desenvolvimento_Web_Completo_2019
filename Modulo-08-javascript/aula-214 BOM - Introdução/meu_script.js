//BOM - Browser Object Model
/*
é uma forma de se comunicar atraves do javascript com recurso do browser propriamente dito.
exemplo:
-acessar o historico de navegação
-força o redirecinamento de URLs
-salvar e recuperar cookies
Essa acções não estão ligadas a elemento html, mas sim acções disparadas atraves de recursos do proprio browser.Basicamente falando, quando os nosso script  html são interpretado pelo browser, nos podemos atraves do javascript acessar tanto os elemento html da pagina com API DOM qaunto acessar recurso do proprio browser atraves da API BOM.
Com isto podemos manipular recurso interno e externo da nossa aplicação web

*/