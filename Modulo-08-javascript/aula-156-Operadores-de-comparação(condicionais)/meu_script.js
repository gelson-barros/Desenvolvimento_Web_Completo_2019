//operadores:
//Igual (==) - Verifica se os valores comparados são iguais
//Idêntico (===) - Verifica se os valores comparados são iguais e do mesmo tipo
//Diferente (!=) - Verifica se os valores comparados são diferentes
//Não Idêntico (!==) - Verifica se os valores comparados são diferentes e de tipos diferentes
//Menor (<) - Verifica se o valor da esquerda é menor que o valor da direito
//Maior (>) - Verifica se o valor da esquerda é maior que o valor da direito
//Menor igual (<=) - Verifica se o valor da esquerda é menor ou igual que o valor da direito
//Maior igual (>=) - Verifica se o valor da esquerda é maior ou igual que o valor da direito